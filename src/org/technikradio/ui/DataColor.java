package org.technikradio.ui;

public class DataColor {
	private int A;
	private int R;
	private int G;
	private int B;
	/**
	 * @return the a
	 */
	public int getA() {
		return A;
	}
	/**
	 * @param a the a to set
	 */
	public void setA(int a) {
		A = a;
	}
	/**
	 * @return the r
	 */
	public int getR() {
		return R;
	}
	/**
	 * @param r the r to set
	 */
	public void setR(int r) {
		R = r;
	}
	/**
	 * @return the g
	 */
	public int getG() {
		return G;
	}
	/**
	 * @param g the g to set
	 */
	public void setG(int g) {
		G = g;
	}
	/**
	 * @return the b
	 */
	public int getB() {
		return B;
	}
	/**
	 * @param b the b to set
	 */
	public void setB(int b) {
		B = b;
	}
	
	public DataColor(){}
}
